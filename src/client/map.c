void init_map()
{
    dirt = load_texture("../../res/dirt.png");
    grass = load_texture("../../res/grass.png");
    water = load_texture("../../res/water.png");

    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;
    dest.x = dest.y = 0;

    load_map(map);
};
void load_map(int arr[20][25])
{
    for (int row = 0; row < 20; row++)
    {
        for (int column = 0; column < 25; column++)
        {
            map[row][column] = arr[row][column];
        }
    }
};
