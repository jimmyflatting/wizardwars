void render_map()
{
    int type = 0;
    for (int row = 0; row < 20; row++)
    {
        for (int column = 0; column < 25; column++)
        {
            type = map[row][column];

            dest.x = column * 32;
            dest.y = row * 32;

            switch (type)
            {
            case 0:
                draw_texture(dirt, src, dest);
                break;
            case 1:
                draw_texture(grass, src, dest);
                break;
            case 2:
                draw_texture(water, src, dest);
                break;
            default:
                break;
            }
        }
    }
};